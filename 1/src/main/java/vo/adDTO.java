package vo;

public class adDTO {
	private int ad_index;
	private int ad_pd_num;
	private String ad_file;
	private String ad_subject;
	private String ad_content;
	public int getAd_index() {
		return ad_index;
	}
	public void setAd_index(int ad_index) {
		this.ad_index = ad_index;
	}
	public int getAd_pd_num() {
		return ad_pd_num;
	}
	public void setAd_pd_num(int ad_pd_num) {
		this.ad_pd_num = ad_pd_num;
	}
	public String getAd_file() {
		return ad_file;
	}
	public void setAd_file(String ad_file) {
		this.ad_file = ad_file;
	}
	public String getAd_subject() {
		return ad_subject;
	}
	public void setAd_subject(String ad_subject) {
		this.ad_subject = ad_subject;
	}
	public String getAd_content() {
		return ad_content;
	}
	public void setAd_content(String ad_content) {
		this.ad_content = ad_content;
	}
}
